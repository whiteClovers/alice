package com.iwonder.alice.framework.vo;

import java.util.List;

public class PageViewEasyUIWrapper {

	private PageView pageView;

	public PageViewEasyUIWrapper(PageView pageView) {
		this.pageView = pageView;
	}

	public int getTotal() {
		return pageView.getRecordCount();
	}

	public List getRows() {
		return pageView.getList();
	}
}
